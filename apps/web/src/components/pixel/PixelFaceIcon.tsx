const SPRITES: Record<string, PX[][]> = {
  lightbulb: FACE_LIGHTBULB,
  mask: FACE_MASK,
  check: FACE_CHECK,
  gear: FACE_GEAR,
  briefcase: FACE_BRIEFCASE,
};

interface PixelFaceIconProps {
  face: keyof typeof SPRITES;
  size?: number; // display size in CSS px (default 32)
  className?: string;
  style?: React.CSSProperties;
}

export const PixelFaceIcon = memo(function PixelFaceIcon({
  face,
  size = 32,
  className,
  style,
}: PixelFaceIconProps) {
  const canvasRef = useRef<HTMLCanvasElement>(null);

  useEffect(() => {
    const canvas = canvasRef.current;
    if (!canvas) return;
    const ctx = canvas.getContext("2d");
    if (!ctx) return;

    const sprite = SPRITES[face];
    if (!sprite) return;

    canvas.width = 16 * SCALE;
    canvas.height = 16 * SCALE;
    ctx.clearRect(0, 0, canvas.width, canvas.height);

    for (let y = 0; y < sprite.length; y++) {
      for (let x = 0; x < sprite[y].length; x++) {
        const px = sprite[y][x];
        if (px === "_") continue;
        const color = C[px];
        if (!color) continue;
        ctx.fillStyle = color;
        ctx.fillRect(x * SCALE, y * SCALE, SCALE, SCALE);
      }
    }
  }, [face]);

  return (
    <canvas
      ref={canvasRef}
      className={`render-pixel ${className ?? ""}`}
      style={{ width: size, height: size, ...style }}
    />
  );
});
