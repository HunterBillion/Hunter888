export type PixelIconName = keyof typeof ICONS;

/**
 * Returns sorted list of icon names — for storybook-like pages.
 */
export function listPixelIcons(): PixelIconName[] {
  return Object.keys(ICONS).sort() as PixelIconName[];
}

interface Props {
  name: PixelIconName | string;
  /** Visual size in CSS pixels (icon scales preserving aspect ratio). */
  size?: number;
  /** Main fill color (CSS). */
  color?: string;
  /** Optional aria-label — set if icon carries semantic meaning. */
  label?: string;
}

export function PixelIcon({ name, size = 24, color = "currentColor", label }: Props) {
  const sprite = ICONS[name as PixelIconName];
  if (!sprite) return null;
  const cell = 100 / 16;
  const rects: React.ReactElement[] = [];
  for (let y = 0; y < sprite.length; y += 1) {
    const row = sprite[y];
    for (let x = 0; x < row.length; x += 1) {
      const ch = row[x];
      let fill: string | null = null;
      if (ch === "#") fill = color;
      else if (ch === "o") fill = `color-mix(in srgb, ${color} 60%, transparent)`;
      else if (ch === "x") fill = `color-mix(in srgb, ${color} 40%, #000)`;
      if (!fill) continue;
      rects.push(
        <rect
          key={`${x}-${y}`}
          x={x * cell + "%"}
          y={y * cell + "%"}
          width={cell + "%"}
          height={cell + "%"}
          fill={fill}
        />,
      );
    }
  }
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 100 100"
      preserveAspectRatio="xMidYMid meet"
      style={{ imageRendering: "pixelated", display: "inline-block", flexShrink: 0 }}
      role={label ? "img" : undefined}
      aria-label={label}
      aria-hidden={label ? undefined : true}
    >
      {rects}
    </svg>
  );
}
