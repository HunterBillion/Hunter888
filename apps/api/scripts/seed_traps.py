async def seed():
    """Main seed function."""
    async with engine.begin() as conn:
        await conn.run_sync(Base.metadata.create_all)

    async with async_session() as db:
        await _seed_traps(db)
        await _seed_objection_chains(db)
        await _seed_cascades(db)
        await db.commit()

    print("\n✓ Traps, chains, and cascades seeded successfully!")


async def _seed_traps(db: AsyncSession):
    """Seed 100 traps."""
    traps = []
    trap_map = {}  # For cascade linking

    for trap_data in TRAPS_DATA:
        trap = Trap(
            id=trap_uuid(trap_data["code"]),
            name=trap_data["name"],
            category=trap_data["category"].value,
            subcategory=trap_data.get("subcategory"),
            difficulty=trap_data["difficulty"],
            detection_level=_get_detection_level(trap_data["difficulty"]),
            client_phrase=trap_data["client_phrase"],
            client_phrase_variants=trap_data.get("client_phrase_variants", []),
            wrong_response_keywords=trap_data.get("wrong_response_keywords", []),
            correct_response_keywords=trap_data.get("correct_response_keywords", []),
            wrong_response_example=trap_data.get("wrong_response_example"),
            correct_response_example=trap_data.get("correct_response_example"),
            penalty=trap_data.get("penalty", -3),
            bonus=trap_data.get("bonus", 2),
            explanation=trap_data.get("explanation"),
            law_reference=trap_data.get("law_reference"),
            archetype_codes=trap_data.get("archetype_codes", []),
            emotion_states=trap_data.get("emotion_states", []),
            fell_emotion_trigger=trap_data.get("fell_emotion_trigger"),
            dodged_emotion_trigger=trap_data.get("dodged_emotion_trigger"),
            is_active=True,
        )
        traps.append(trap)
        trap_map[trap_data["code"]] = trap

    db.add_all(traps)
    await db.flush()
    print(f"  Traps: {len(traps)} created")

    return trap_map


def _get_detection_level(difficulty: int) -> str:
    """Derive detection level from difficulty."""
    if difficulty <= 3:
        return "keyword"
    elif difficulty <= 7:
        return "regex"
    else:
        return "llm"


