# ═══════════════════════════════════════════════════════════════════════════════
# SEED FUNCTION
# ═══════════════════════════════════════════════════════════════════════════════

async def seed_legal_knowledge() -> None:
    """Populate legal_knowledge_chunks with verified 127-ФЗ legal facts."""

    logger.info("=" * 70)
    logger.info("  Seeding 127-ФЗ legal knowledge base")
    logger.info("=" * 70)

    async with async_session() as session:
        # ── Check for existing data ────────────────────────────────────────
        result = await session.execute(
            select(func.count()).select_from(LegalKnowledgeChunk)
        )
        existing_count = result.scalar() or 0

        if existing_count > 0:
            logger.info(
                "  ℹ  Legal knowledge chunks already exist (%d rows). "
                "Skipping seed to avoid duplicates.",
                existing_count,
            )
            return

        # ── Insert all facts ───────────────────────────────────────────────
        total = len(LEGAL_FACTS)
        logger.info("  → Inserting %d legal knowledge chunks...", total)

        category_counts: dict[str, int] = {}

        for idx, fact in enumerate(LEGAL_FACTS, start=1):
            chunk = LegalKnowledgeChunk(
                category=fact["category"],
                fact_text=fact["fact_text"],
                law_article=fact["law_article"],
                common_errors=fact["common_errors"],
                match_keywords=fact["match_keywords"],
                correct_response_hint=fact["correct_response_hint"],
                error_frequency=fact["error_frequency"],
                is_active=True,
            )
            session.add(chunk)

            cat_name = fact["category"].value
            category_counts[cat_name] = category_counts.get(cat_name, 0) + 1

            if idx % 20 == 0 or idx == total:
                logger.info("    [%3d/%3d] chunks staged...", idx, total)

        # ── Commit transaction ─────────────────────────────────────────────
        await session.commit()
        logger.info("  ✓ Committed %d chunks to database.", total)

        # ── Summary ────────────────────────────────────────────────────────
        logger.info("")
        logger.info("  Category breakdown:")
        for cat, count in sorted(category_counts.items()):
            logger.info("    %-15s : %d facts", cat, count)
        logger.info("")
        logger.info("  Seed complete.")
        logger.info("=" * 70)


# ═══════════════════════════════════════════════════════════════════════════════
# ENTRYPOINT
# ═══════════════════════════════════════════════════════════════════════════════

if __name__ == "__main__":
    asyncio.run(seed_legal_knowledge())
