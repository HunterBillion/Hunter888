@dataclass
class ConsequenceEvent:
    """Consequence generated by agent actions, processed by Game Director."""
    id: str = field(default_factory=lambda: str(uuid.uuid4()))
    level: str = CONSEQUENCE_LOCAL  # cosmetic | local | global
    trigger_action: str = ""
    effect_description: str = ""
    applied_at: datetime = field(default_factory=datetime.utcnow)
    expires_at: Optional[datetime] = None
    is_active: bool = True
    source_agent: str = ""  # "agent_1", "agent_3", etc.


@dataclass
class StoryletActivation:
    """A storylet that has been activated for a client."""
    storylet_code: str = ""
    narrative_text: str = ""
    effects: dict = field(default_factory=dict)
    priority: int = 5
    activated_at: datetime = field(default_factory=datetime.utcnow)


@dataclass
class BetweenCallEvent:
    """Event generated between calls for the frontend panel."""
    event_type: str = "message"  # message | status_change | consequence | storylet | callback
    title: str = ""
    content: str = ""
    game_timestamp: datetime = field(default_factory=datetime.utcnow)
    payload: dict = field(default_factory=dict)


@dataclass
class ContextInjection:
    """Three-tier context assembled for LLM prompt injection."""
    tier1_identity: str = ""    # archetype + emotion + personality + factors (~500 tokens)
    tier2_memory: str = ""      # past calls + consequences + promises (~800 tokens)
    tier3_situational: str = "" # storylets + events + reputation (~400 tokens)
    total_tokens: int = 0
    active_factors: list = field(default_factory=list)
    active_storylets: list = field(default_factory=list)
    active_consequences: list = field(default_factory=list)


@dataclass
class SessionResult:
    """Result of a completed training session, sent by Agent 1."""
    session_id: str = ""
    client_story_id: str = ""
    final_emotion_state: str = "cold"
    score_total: float = 0.0
    score_breakdown: dict = field(default_factory=dict)
    traps_fell: list = field(default_factory=list)
    traps_dodged: list = field(default_factory=list)
    promises_made: list = field(default_factory=list)
    promises_broken: list = field(default_factory=list)
    key_moments: list = field(default_factory=list)
    stage_directions: list = field(default_factory=list)
    duration_seconds: int = 0
    empathy_detected: bool = False
    rudeness_detected: bool = False
    legal_errors: list = field(default_factory=list)


