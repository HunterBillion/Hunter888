"""
Game Director Engine — Central Orchestrator for Hunter888 v5.

This is the BRAIN of the entire narrative game system.
It connects all 8 agents into a unified story flow:

  Agent 1 (Roleplay) ←→ Game Director ←→ Agent 3 (Traps)
  Agent 2 (Scoring)  ←→ Game Director ←→ Agent 4 (Voice)
  Agent 5 (Gamification) ←→ Game Director ←→ Agent 6 (Generator)
  Agent 7 (CRM)      ←→ Game Director ←→ Agent 8 (PvP)

Data flow:
  1. Manager starts session → GD loads ClientStory from DB
  2. GD builds context injection (3-tier) → injected into LLM prompt
  3. During session: stage directions analyzed for consequences
  4. After session: GD advances story, evaluates storylets, generates events
  5. Between calls: GD generates frontend panel events

All inter-agent communication flows through this module.
Agents do NOT import each other — they import game_director.
"""

